			<strong id="title">Frequently Asked Questions</strong><br/><br/> 
			<strong>What kind of candidates are you looking for?</strong><br/>
			All kinds! Impact Apprentice to us doesn't mean we are only looking for business students. Each year we have had participants from across Canada, differing ages and studying in broad ﬁelds (e.g. business, engineering, science, arts, math and the list goes on!)<br/><br/>
			<strong>What can we expect in the four-days of competition?</strong> <br/>
			During the day competitors work in teams of 8 to solve the challenge presented by a sponsoring company. Challenges can range from running their stores for the day, preparing a marketing plan in 6 hours to negotiating with street people to trade dollarstore items! We are by no means a traditional case competition. There are also surprise and mini challenges throughout the four days. During the evenings there will be upscale dinners and social events to network with fellow competitors and industry professionals.<br/><br/> 
			<strong>How do you become the Apprentice?</strong><br/>
Apprentice tests you not only on your individual skills but working as a team. 
Teams are awarded points based on their standings for a challenge. But each members 
of a team is collecting points individually, so only one competitor can become the 
Apprentice. <br/><br/>
<strong>Iʼm really interested but unsure if I can make a great video for round two...</strong><br/>
The purpose of the challenge is not about who can make the best looking video. We 
(the judges) are looking for content, individual personality and how you approach the 
challenge. <br/><br/>
*If you do enjoy making videos then by all means go all out as this is part of your 
individual personality and experience <br/><br/>
			<strong>I am applying as a student living outside of Ontario, how will I get to Toronto?</strong><br/>
We will work hard with delegates to secure subsidies for covering expenses such as 
travel. We do our best to lower costs for you as much as possible. However, travel 
arrangements are the responsibility of the student.<br/><br/>
<strong>Where will I be staying in downtown Toronto? How many nights are covered?</strong><br/>
Hotel will be announced once they are conﬁrmed and booked (all choices are located in 
the core of Downtown Toronto). Delegates will be staying for 4 nights (the last night is 
optional and delegates may check out early if they need to). <br/><br/>
	
